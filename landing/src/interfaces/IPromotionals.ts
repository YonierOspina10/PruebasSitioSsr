/**
 * Interface representing a promotional menu item.
 */
export interface Promotional {
    /** Optional icon for the menu item. */
    MENU_ICON?: string;

    /** Optional identifier for the menu item. */
    MENU_ID?: string;

    /** Optional URL for the menu item's image. */
    MENU_IMG_URL?: string;

    /** Optional section the menu item belongs to. */
    MENU_SECCION?: string;

    /** Optional title of the menu item. */
    MENU_TITLE?: string;

    /** Optional URL for the menu item. */
    MENU_URL?: string;
}

